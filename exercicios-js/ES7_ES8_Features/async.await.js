// async/await funciona obrigatóriamente no protocolo de promises
// async é uma palavra chave usada no inicio de uma função
// await é usado para avisar a função para esperar o resultado da promise
// await só pode ser usado para uma função async
// async/await já está funcionando em quase todos os navegadores e no Node.js

// Vantagens de usar async/await

// código mais simplificado
// Mais fácil de debugar por ter menos callbacks
// A conversão de uma promise para async/await é bem simples
// O código fica com menos encadeamentos
