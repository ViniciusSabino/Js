// Atribuição

const a = 7;
let b = 3;

// Acrescentando o valor de A mais o valor de B

b += a; // 10

// Exemplos com outras operações

b -= 1; // Resultado = 9
console.log(b);

b *= 20; // Resultado = 180
console.log(b);

b /= 4; // Resultado = 45
console.log(b);

b %= 2; // B = B MOD 2 = Resultado = 1
console.log(b);


